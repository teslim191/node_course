const os = require('os')

// platform
console.log(os.platform())

// uptime
console.log(os.uptime())

// home directory
console.log(os.homedir())

// total memory
console.log(os.totalmem())

// free memory
console.log(os.freemem())

// cpus core
console.log(os.cpus())

// cpu arch
console.log(os.arch())